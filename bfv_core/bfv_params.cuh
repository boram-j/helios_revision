// Compile-time limits
// ---------------------------------------------------------------------------
static constexpr int MAX_RNS_PRIMES = 8;   // max primes in base Q or special P
static constexpr int MAX_POLY_DEG   = 32768;

// ---------------------------------------------------------------------------
